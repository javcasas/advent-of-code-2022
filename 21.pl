  find_monkey_var(Cache, Name, Var, Cache1),
  find_monkey_var(Cache1, X, VarX, Cache2),
  find_monkey_var(Cache2, Y, VarY, Cache3),
  Var #= VarX / VarY.

process_monkeys_(Cache, [], Cache).
process_monkeys_(Cache, [monkey(Name, Operation)|Ms], Cache2) :-
  monkey_operation_order(Name, Operation, Cache, Cache1),
  process_monkeys_(Cache1, Ms, Cache2).
  

process_monkeys(Ms, Result) :-
  process_monkeys_([], Ms, Result).


