write_declares([], []).
write_declares([monkey(Name, _)|Xs], [L|Ls]) :-
  phrase(format_("(declare-const ~s Int)\n", [Name]), L),
  write_declares(Xs, Ls).

write_assert(monkey(Name, constant(X)), Ls) :-
  phrase(format_("(assert (= ~s ~d))\n", [Name, X]), Ls).
write_assert(monkey(Name, add(X, Y)), Ls) :-
  phrase(format_("(assert (= ~s (+ ~s ~s)))\n", [Name, X, Y]), Ls).
write_assert(monkey(Name, sub(X, Y)), Ls) :-
  phrase(format_("(assert (= ~s (- ~s ~s)))\n", [Name, X, Y]), Ls).
write_assert(monkey(Name, mul(X, Y)), Ls) :-
  phrase(format_("(assert (= ~s (* ~s ~s)))\n", [Name, X, Y]), Ls).
write_assert(monkey(Name, div(X, Y)), Ls) :-
  phrase(format_("(assert (= ~s (/ ~s ~s)))\n", [Name, X, Y]), Ls).

write_asserts([], []).
write_asserts([A|Xs], [L|Ls]) :-
  write_assert(A, L),
  write_asserts(Xs, Ls).

write_smtlib(Ms, R1) :-
  write_declares(Ms, Declares),
  write_asserts(Ms, Asserts),
  Extra = ["(check-sat)\n", "(get-value (root))\n"],
  append([Declares, Asserts, Extra], R),
  append(R, R1).

