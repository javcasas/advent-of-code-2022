digit('0') --> "0".
digit('1') --> "1".
digit('2') --> "2".
digit('3') --> "3".
digit('4') --> "4".
digit('5') --> "5".
digit('6') --> "6".
digit('7') --> "7".
digit('8') --> "8".
digit('9') --> "9".
digits_([X]) --> digit(X).
digits_([X1|Xs]) --> digit(X1), digits_(Xs).
digits(X) --> digits_(Xs), {number_chars(X, Xs)}.
digits(X) --> "-", digits_(Xs), {number_chars(X1, Xs), X is -X1}.

letter(X) --> [X], {char_code('a', A), char_code('z', Z), char_code(X, C), C >= A, C=<Z}.

monkey_name([A,B,C,D]) --> letter(A), letter(B), letter(C), letter(D).

monkey_operation(constant(X)) --> digits(X).
monkey_operation(add(X, Y)) --> monkey_name(X), " + ", monkey_name(Y).
monkey_operation(sub(X, Y)) --> monkey_name(X), " - ", monkey_name(Y).
monkey_operation(mul(X, Y)) --> monkey_name(X), " * ", monkey_name(Y).
monkey_operation(div(X, Y)) --> monkey_name(X), " / ", monkey_name(Y).

parse_monkey(monkey(Name, Operation)) --> monkey_name(Name), ": ", monkey_operation(Operation), "\n".

parse_monkeys([]) --> "\n".
parse_monkeys([X|Xs]) --> parse_monkey(X), parse_monkeys(Xs).

find_monkey_var(Cache, Name, Var, Cache) :-
  select(monkey(Name, Var), Cache, Cache1).
find_monkey_var(Cache, Name, Var, [monkey(Name, Var)|Cache]) :-
  \+ select(monkey(Name, _), Cache, _),
  Var in inf..sup.

monkey_operation_order(Name, constant(X), Cache, Cache1) :-
  find_monkey_var(Cache, Name, Var, Cache1),
  Var #= 1.
monkey_operation_order(Name, add(X, Y), Cache, Cache3) :-
  find_monkey_var(Cache, Name, Var, Cache1),
  find_monkey_var(Cache1, X, VarX, Cache2),
  find_monkey_var(Cache2, Y, VarY, Cache3),
  Var #= max(VarX + 1, VarY + 1).
monkey_operation_order(Name, sub(X, Y), Cache, Cache3) :-
  find_monkey_var(Cache, Name, Var, Cache1),
  find_monkey_var(Cache1, X, VarX, Cache2),
  find_monkey_var(Cache2, Y, VarY, Cache3),
  Var #= max(VarX + 1, VarY + 1).
monkey_operation_order(Name, mul(X, Y), Cache, Cache3) :-
  find_monkey_var(Cache, Name, Var, Cache1),
  find_monkey_var(Cache1, X, VarX, Cache2),
  find_monkey_var(Cache2, Y, VarY, Cache3),
  Var #= max(VarX + 1, VarY + 1).
monkey_operation_order(Name, div(X, Y), Cache, Cache3) :-
  find_monkey_var(Cache, Name, Var, Cache1),
  find_monkey_var(Cache1, X, VarX, Cache2),
  find_monkey_var(Cache2, Y, VarY, Cache3),
  Var #= max(VarX + 1, VarY + 1).

monkey_operation1(Name, constant(X), Cache, Cache1) :-
  find_monkey_var(Cache, Name, Var, Cache1),
  Var #= X.
monkey_operation1(Name, add(X, Y), Cache, Cache3) :-
  find_monkey_var(Cache, Name, Var, Cache1),
  find_monkey_var(Cache1, X, VarX, Cache2),
  find_monkey_var(Cache2, Y, VarY, Cache3),
  Var #= VarX + VarY.
monkey_operation1(Name, sub(X, Y), Cache, Cache3) :-
  find_monkey_var(Cache, Name, Var, Cache1),
  find_monkey_var(Cache1, X, VarX, Cache2),
  find_monkey_var(Cache2, Y, VarY, Cache3),
  Var #= VarX - VarY.
monkey_operation1(Name, mul(X, Y), Cache, Cache3) :-
  find_monkey_var(Cache, Name, Var, Cache1),
  find_monkey_var(Cache1, X, VarX, Cache2),
  find_monkey_var(Cache2, Y, VarY, Cache3),
  Var #= VarX * VarY.
